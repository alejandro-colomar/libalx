#include <stdio.h>


/******************************************************************************
 ******* define ***************************************************************
 ******************************************************************************/
/* Rename without alx_ prefix */
#if defined(ALX_NO_PREFIX)
#define seekc(c)		alx_seekc(c)
#define fseekc(stream, c)	alx_fseekc(stream, c)
#define wait4enter()		alx_wait4enter()
#endif	/* defined(ALX_NO_PREFIX) */


/******************************************************************************
 ******* enum *****************************************************************
 ******************************************************************************/


/******************************************************************************
 ******* struct / union ******************************************************
 ******************************************************************************/


/******************************************************************************
 ******* prototypes ***********************************************************
 ******************************************************************************/
__attribute__((always_inline))
inline
int	alx_seekc	(char c);
__attribute__((nonnull))
inline
int	alx_fseekc	(FILE *stream, char c);
__attribute__((always_inline))
inline
int	alx_wait4enter	(void);


/******************************************************************************
 ******* inline ***************************************************************
 ******************************************************************************/
inline
int	alx_seekc	(char c)
{
	return	alx_fseekc(stdin, c);
}

inline
int	alx_fseekc	(FILE *stream, char c)
{
	int ch;

	do {
		ch = fgetc(stream);
	} while ((ch != c)  &&  (ch != EOF));

	return	(ch == EOF);
}

inline
int	alx_wait4enter	(void)
{
	return	alx_seekc('\n');
}


