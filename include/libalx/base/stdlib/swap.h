#define ALX_SWAP(a_ptr, b_ptr)	do					\
{									\
	__auto_type	a_ptr_ = (a_ptr);				\
	__auto_type	b_ptr_ = (b_ptr);				\
	typeof(*a_ptr_)	tmp_;						\
									\
	alx_Static_assert_compatible(a_ptr_, b_ptr_);			\
									\
	tmp_	= *a_ptr_;						\
	*a_ptr_	= *b_ptr_;						\
	*b_ptr_	= tmp_;							\
} while (0)


/* Rename without alx_ prefix */
#if defined(ALX_NO_PREFIX)
#define SWAP(a_ptr, b_ptr)	ALX_SWAP(a_ptr, b_ptr)
#endif


