#define BITFIELD_SET(x, pos, len)		(			\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	p_	= (pos);				\
	__auto_type	l_	= (len);				\
									\
	alx_Static_assert_unsigned(*x_);				\
	alx_Static_assert_bit_fits_type(p_ + l_ - 1, *x_);		\
									\
	*x_	|= BITMASK_GEN(p_, l_);					\
	*x_;								\
}									\
)

#define BITFIELD_CLEAR(x, pos, len)		(			\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	p_	= (pos);				\
	__auto_type	l_	= (len);				\
									\
	alx_Static_assert_unsigned(*x_);				\
	alx_Static_assert_bit_fits_type(p_ + l_ - 1, *x_);		\
									\
	*x_	&= ~BITMASK_GEN(p_, l_);				\
	*x_;								\
}									\
)

#define BITFIELD_FLIP(x, pos, len)		(			\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	p_	= (pos);				\
	__auto_type	l_	= (len);				\
									\
	alx_Static_assert_unsigned(*x_);				\
	alx_Static_assert_bit_fits_type(p_ + l_ - 1, *x_);		\
									\
	*x_	^= BITMASK_GEN(p_, l_);					\
	*x_;								\
}									\
)

#define BITFIELD_READ(x, pos, len)		(			\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	p_	= (pos);				\
	__auto_type	l_	= (len);				\
									\
	alx_Static_assert_unsigned(x_);					\
	alx_Static_assert_bit_fits_type(p_ + l_ - 1, x_);		\
									\
	x_	&= BITMASK_GEN(p_, l_);					\
	x_	>>= p_;							\
	x_;								\
}									\
)

#define BITFIELD_WRITE(x, pos, len, field)	(			\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	p_	= (pos);				\
	__auto_type	l_	= (len);				\
	__auto_type	f_	= (field);				\
									\
	alx_Static_assert_unsigned(*x_);				\
	alx_Static_assert_unsigned(f_);					\
	alx_Static_assert_bit_fits_type(p_ + l_ - 1, *x_);		\
									\
	f_	&= BITMASK_GEN(0, l_);					\
	f_	<<= p_;							\
	*x_	|= f_;							\
	*x_;								\
}									\
)


