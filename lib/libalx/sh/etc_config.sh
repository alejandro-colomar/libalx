## sudo
function alx_etc_config()
{
	local	config="$1";
	local	val="$2";
	local	file="$3";

	echo "	${config}	${val}";
	sed -i	"/^[ \t#]*${config}/s/^.*$/${config} ${val}/"  "${file}";
	grep -q	"^${config} ${val}$"  "${file}"				\
	|| echo	"${config} ${val}" >> "${file}";
}


