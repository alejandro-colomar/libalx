################################################################################
##	definitions							      ##
################################################################################
ARGC=2;


################################################################################
##	main								      ##
################################################################################
function main()
{
	local	mode="$1";
	local	stack="$2";

	alx_stack_delete	"${mode}" "${stack}";
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo >&2							\
'Usage: /usr/local/libexec/libalx/stack_delete.sh mode stackname
Mode:
	kubernetes
	openshift
	swarm';
	exit	${EX_USAGE};
fi

main	"$1" "$2";


