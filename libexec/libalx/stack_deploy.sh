################################################################################
##	definitions							      ##
################################################################################
ARGC=3;


################################################################################
##	main								      ##
################################################################################
function main()
{
	local	mode="$1";
	local	project="$2";
	local	stack="$3";

	alx_stack_deploy	"${mode}" "${project}" "${stack}";
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo >&2							\
'Usage: /usr/local/libexec/libalx/stack_deploy.sh mode projectname stackname
Mode:
	kubernetes
	openshift
	swarm';
	exit	${EX_USAGE};
fi

main	"$1" "$2" "$3";


