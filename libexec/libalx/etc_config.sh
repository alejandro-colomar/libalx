source	/usr/local/lib/libalx/sh/sysexits.sh;


################################################################################
##	definitions							      ##
################################################################################
ARGC=3;


################################################################################
##	main								      ##
################################################################################
function main()
{
	local	config="$1";
	local	val="$2";
	local	file="$3";

	sed -i	"/^[ \t#]*${config}/s/^.*$/${config} ${val}/"  "${file}";
	grep -q	"^${config} ${val}$"  "${file}"				\
	|| echo	"${config} ${val}" >> "${file}";
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo >&2 "Usage: $0 key value file";
	exit	${EX_USAGE};
fi

main	"$1" "$2" "$3";


