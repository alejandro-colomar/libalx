################################################################################
##	definitions							      ##
################################################################################


################################################################################
##	main								      ##
################################################################################
function main()
{
	local	fname=$@;

	sed 's/include "/include </'	-i ${fname};
	sed '/include </s/"/>/'		-i ${fname};
}


################################################################################
##	run								      ##
################################################################################

main	"$@";


