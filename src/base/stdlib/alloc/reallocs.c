void	*alx_reallocs__	(void *restrict ptr, size_t size, int *restrict error)
{
	void	*p;

	*error	= 0;
	if (!size)
		goto zero;

	p	= realloc(ptr, size);
	if (!p)
		goto err;

	return	p;
err:
	*error	= -ENOMEM;
	return	ptr;
zero:
	free(ptr);
	*error	= ENOMEM;
	return	NULL;
}


