void	*alx_mallocs__	(size_t size, int *error)
{
	void	*p;

	*error	= 0;
	if (!size)
		goto zero;

	p	= malloc(size);
	if (!p)
		goto err;

	return	p;
err:
	*error	= -ENOMEM;
	return	NULL;
zero:
	*error	= ENOMEM;
	return	NULL;
}


