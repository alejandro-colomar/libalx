void	*alx_callocs__	(ptrdiff_t nmemb, size_t size, int *error)
{
	void	*p;

	*error	= 0;
	if (!nmemb || !size)
		goto zero;
	if (nmemb < 0)
		goto ovf;

	p	= calloc(nmemb, size);
	if (!p)
		goto err;

	return	p;
ovf:
	errno	= ENOMEM;
err:
	*error	= -ENOMEM;
	return	NULL;
zero:
	*error	= ENOMEM;
	return	NULL;
}


