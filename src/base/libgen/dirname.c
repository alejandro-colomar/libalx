int	alx_dirname_s	(char dest[static restrict FILENAME_MAX],
			 const char *restrict path)
{
	char	tmp[FILENAME_MAX];

	if (alx_strlcpys(tmp, path, ARRAY_SIZE(tmp), NULL))
		return	-1;
	if (alx_strlcpys(dest, dirname(tmp), FILENAME_MAX, NULL))
		return	-1;

	return	0;
}


