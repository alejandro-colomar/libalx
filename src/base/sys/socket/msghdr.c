void	alx_wrap_msghdr	(struct msghdr *restrict msg,
			 struct iovec *restrict iov,
			 size_t bufsiz, void *restrict buf,
			 size_t cbufsiz, void *restrict cbuf)
{

	iov->iov_base	= buf;
	iov->iov_len	= bufsiz;

	msg->msg_name		= NULL;
	msg->msg_iov		= iov;
	msg->msg_iovlen		= 1;
	msg->msg_control	= cbuf;
	msg->msg_controllen	= cbufsiz;
}


