int	alx_strlcpys		(char dest[restrict /*size*/],
				 const char src[restrict /*size*/],
				 ptrdiff_t size, ptrdiff_t *restrict len)
{
	ptrdiff_t	l;

	if (size <= 0)
		return	-E2BIG;

	l	= strlcpy(dest, src, size);
	if (len)
		*len	= l;

	if (l >= size)
		return	ENOBUFS;

	return	0;
}


