################################################################################
##	source								      ##
################################################################################
source	lib/libalx/sh/sysexits.sh;
source	lib/libalx/sh/include-quotes-to-angles.sh;


################################################################################
##	definitions							      ##
################################################################################
ARGC=0;


################################################################################
##	functions							      ##
################################################################################


################################################################################
##	main								      ##
################################################################################
function main()
{

	export -f include_quotes_to_angles;

	find . -name "*.h" -exec bash -c 'include_quotes_to_angles $1' - '{}' \;;
	find . -name "*.hxx" -exec bash -c 'include_quotes_to_angles $1' - '{}' \;;
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo	"Illegal number of parameters (Requires ${ARGC})";
	exit	${EX_USAGE};
fi

main;


